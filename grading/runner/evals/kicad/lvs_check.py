"""KiCad LVS Check Verifier - Layout vs Schematic parity verification.

Reads the LVS report (generated by kicad-cli pcb drc --schematic-parity)
from the snapshot and checks for zero parity violations.
"""

import zipfile

from runner.evals.kicad.common import find_report
from runner.evals.models import EvalImplInput
from runner.models import VerifierResult, VerifierResultStatus


async def kicad_lvs_check_eval(input: EvalImplInput) -> VerifierResult:
    """Verify that PCB layout matches the schematic (Layout vs Schematic).

    Reads the LVS report generated by kicad-cli pcb drc --schematic-parity.
    Checks for zero schematic parity violations.
    """
    verifier_id = input.verifier.verifier_id
    verifier_version = input.verifier.verifier_version
    strict_mode = str(
        input.verifier.verifier_values.get("strict_mode", "true")
    ).lower() in (
        "true",
        "1",
        "yes",
    )
    project_name = input.verifier.verifier_values.get("project_name")

    if not input.final_snapshot_bytes:
        return VerifierResult(
            verifier_id=verifier_id,
            verifier_version=verifier_version,
            score=0.0,
            status=VerifierResultStatus.ERROR,
            verifier_result_values={},
            message="No final snapshot available",
        )

    try:
        input.final_snapshot_bytes.seek(0)
        with zipfile.ZipFile(input.final_snapshot_bytes, "r") as snapshot_zip:
            report = find_report(snapshot_zip, "_lvs.json", project_name)
    except zipfile.BadZipFile:
        return VerifierResult(
            verifier_id=verifier_id,
            verifier_version=verifier_version,
            score=0.0,
            status=VerifierResultStatus.ERROR,
            verifier_result_values={},
            message="Invalid snapshot zip file",
        )

    if report is None:
        return VerifierResult(
            verifier_id=verifier_id,
            verifier_version=verifier_version,
            score=0.0,
            status=VerifierResultStatus.ERROR,
            verifier_result_values={},
            message="No LVS report found in snapshot. Ensure snapshot.sh runs kicad-cli pcb drc --schematic-parity.",
        )

    all_violations = report.get("violations", [])
    parity_violations = [
        v
        for v in all_violations
        if "parity" in v.get("type", "").lower()
        or "schematic" in v.get("description", "").lower()
    ]
    # Only consider parity-specific violations for LVS; ignore unrelated DRC issues
    relevant = parity_violations
    total_violations = len(relevant)
    errors = sum(1 for v in relevant if v.get("severity") == "error")

    passes = total_violations == 0 if strict_mode else errors == 0
    mismatches = "; ".join(v.get("description", "unknown")[:100] for v in relevant[:5])

    score = 1.0 if passes else 0.0

    return VerifierResult(
        verifier_id=verifier_id,
        verifier_version=verifier_version,
        score=score,
        status=VerifierResultStatus.OK,
        verifier_result_values={
            "passes": passes,
            "mismatch_count": total_violations,
            "error_count": errors,
            "mismatches": mismatches,
        },
        message=f"LVS: {'PASS' if passes else f'FAIL ({total_violations} mismatches)'}",
    )
